# Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos três valores lidos.


num1 = float(input('\nDigite o Primeiro valor: '))
num2 = float(input('Digite o Segundo valor: '))
num3 = float(input('Digite o Terceiro valor: '))

soma = (num1 ** 2) + (num2 ** 2) + (num3 ** 2)

print(f'\nA soma dos quadrados dos trê valores são: {soma}')
